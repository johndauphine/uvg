use std::collections::HashMap;

use crate::cli::DdlOptions;
use crate::codegen::{
    is_primary_key_column, is_serial_default, is_unique_constraint_index, topo_sort_tables,
};
use crate::ddl_typemap;
use crate::dialect::Dialect;
use crate::schema::{
    ColumnInfo, ConstraintInfo, ConstraintType, IntrospectedSchema, TableInfo, TableType,
};

/// Output from DDL generation.
pub enum DdlOutput {
    Single(String),
    Split(Vec<(String, String)>),
}

pub struct DdlGenerator;

impl DdlGenerator {
    pub fn generate(
        &self,
        schema: &IntrospectedSchema,
        target_schema: Option<&IntrospectedSchema>,
        options: &DdlOptions,
    ) -> DdlOutput {
        match target_schema {
            Some(target) => {
                let ddl = diff_schemas(schema, target, options);
                DdlOutput::Single(ddl)
            }
            None => self.generate_full(schema, options),
        }
    }

    fn generate_full(&self, schema: &IntrospectedSchema, options: &DdlOptions) -> DdlOutput {
        let source_dialect = schema.dialect;
        let target_dialect = options.target_dialect;
        let sorted = topo_sort_tables(&schema.tables);

        // Filter to tables only (skip views for DDL)
        let tables: Vec<&&TableInfo> = sorted
            .iter()
            .filter(|t| t.table_type == TableType::Table)
            .collect();

        let mut all_stmts: Vec<String> = Vec::new();
        let mut per_table: Vec<(String, String)> = Vec::new();

        // Detect circular FK dependencies — warn if cycles exist
        let has_cycles = detect_fk_cycles(&schema.tables);

        // Header
        let mut header = format!(
            "-- Generated by uvg\n-- Source: {source_dialect}, Target: {target_dialect}\n"
        );
        if has_cycles {
            header.push_str("-- WARNING: Circular foreign key dependencies detected.\n-- Some FK constraints may reference tables not yet created.\n-- Consider applying FK constraints separately via ALTER TABLE.\n");
        }

        // Enum types (PG target only)
        let enum_stmts = generate_enum_types(schema, target_dialect);

        for table in &tables {
            let mut table_stmts = Vec::new();

            let create = generate_create_table(table, source_dialect, target_dialect, options);
            table_stmts.push(create);

            if !options.noindexes {
                let indexes = generate_indexes(table, target_dialect);
                table_stmts.extend(indexes);
            }

            if !options.nocomments {
                let comments = generate_comments(table, target_dialect);
                table_stmts.extend(comments);
            }

            let table_ddl = table_stmts.join("\n\n");
            all_stmts.push(table_ddl.clone());
            // Disambiguate filenames by schema for multi-schema introspection
            let filename = if table.schema.is_empty()
                || table.schema == target_dialect.default_schema()
            {
                format!("{}.sql", table.name)
            } else {
                format!("{}_{}.sql", table.schema, table.name)
            };
            per_table.push((filename, table_ddl));
        }

        if options.split_tables {
            let mut files: Vec<(String, String)> = Vec::new();

            if !enum_stmts.is_empty() {
                files.push(("_types.sql".to_string(), format!("{header}\n{}", enum_stmts.join("\n\n"))));
            }

            for (filename, content) in per_table {
                files.push((filename, format!("{header}\n{content}\n")));
            }

            // Order file uses the actual filenames from per_table (already schema-qualified)
            let order: Vec<String> = files
                .iter()
                .filter(|(n, _)| n != "_types.sql")
                .map(|(n, _)| n.clone())
                .collect();
            files.push(("_order.txt".to_string(), order.join("\n") + "\n"));

            DdlOutput::Split(files)
        } else {
            let mut full = header;
            if !enum_stmts.is_empty() {
                full.push('\n');
                full.push_str(&enum_stmts.join("\n\n"));
                full.push_str("\n\n");
            }
            full.push('\n');
            full.push_str(&all_stmts.join("\n\n"));
            full.push('\n');
            DdlOutput::Single(full)
        }
    }
}

/// Quote an identifier for the target dialect.
pub fn quote_identifier(name: &str, dialect: Dialect) -> String {
    match dialect {
        Dialect::Postgres | Dialect::Sqlite => format!("\"{}\"", name.replace('"', "\"\"")),
        Dialect::Mysql => format!("`{}`", name.replace('`', "``")),
        Dialect::Mssql => format!("[{}]", name.replace(']', "]]")),
    }
}

/// Generate a CREATE TABLE statement.
fn generate_create_table(
    table: &TableInfo,
    source_dialect: Dialect,
    target_dialect: Dialect,
    options: &DdlOptions,
) -> String {
    let qname = qualified_table_name(&table.schema, &table.name, target_dialect);
    let mut parts: Vec<String> = Vec::new();

    // Detect if any column has inline PK AUTOINCREMENT (SQLite)
    let has_inline_pk = target_dialect == Dialect::Sqlite
        && table.columns.iter().any(|col| {
            let is_auto = col.is_identity
                || col.autoincrement == Some(true)
                || col
                    .column_default
                    .as_deref()
                    .map(|d| is_serial_default(d, source_dialect))
                    .unwrap_or(false);
            is_auto && is_primary_key_column(&col.name, &table.constraints)
        });

    // Columns
    for col in &table.columns {
        parts.push(generate_column_def(
            col,
            &table.constraints,
            source_dialect,
            target_dialect,
        ));
    }

    // Constraints
    if !options.noconstraints {
        // Primary key (suppress if SQLite auto-increment already emitted inline PK)
        if !has_inline_pk {
            for c in &table.constraints {
                if c.constraint_type == ConstraintType::PrimaryKey {
                    let cols: Vec<String> = c
                        .columns
                        .iter()
                        .map(|col| quote_identifier(col, target_dialect))
                        .collect();
                    parts.push(format!(
                        "    CONSTRAINT {} PRIMARY KEY ({})",
                        quote_identifier(&c.name, target_dialect),
                        cols.join(", ")
                    ));
                }
            }
        }

        // Unique constraints
        for c in &table.constraints {
            if c.constraint_type == ConstraintType::Unique {
                let cols: Vec<String> = c
                    .columns
                    .iter()
                    .map(|col| quote_identifier(col, target_dialect))
                    .collect();
                parts.push(format!(
                    "    CONSTRAINT {} UNIQUE ({})",
                    quote_identifier(&c.name, target_dialect),
                    cols.join(", ")
                ));
            }
        }

        // Foreign keys
        for c in &table.constraints {
            if c.constraint_type == ConstraintType::ForeignKey {
                if let Some(ref fk) = c.foreign_key {
                    let cols: Vec<String> = c
                        .columns
                        .iter()
                        .map(|col| quote_identifier(col, target_dialect))
                        .collect();
                    let ref_table =
                        qualified_table_name(&fk.ref_schema, &fk.ref_table, target_dialect);
                    let ref_cols: Vec<String> = fk
                        .ref_columns
                        .iter()
                        .map(|col| quote_identifier(col, target_dialect))
                        .collect();
                    let mut fk_str = format!(
                        "    CONSTRAINT {} FOREIGN KEY ({}) REFERENCES {} ({})",
                        quote_identifier(&c.name, target_dialect),
                        cols.join(", "),
                        ref_table,
                        ref_cols.join(", ")
                    );
                    if fk.delete_rule != "NO ACTION" {
                        fk_str.push_str(&format!(" ON DELETE {}", fk.delete_rule));
                    }
                    if fk.update_rule != "NO ACTION" {
                        fk_str.push_str(&format!(" ON UPDATE {}", fk.update_rule));
                    }
                    parts.push(fk_str);
                }
            }
        }

        // Check constraints
        for c in &table.constraints {
            if c.constraint_type == ConstraintType::Check {
                if let Some(ref expr) = c.check_expression {
                    parts.push(format!(
                        "    CONSTRAINT {} CHECK ({})",
                        quote_identifier(&c.name, target_dialect),
                        expr
                    ));
                }
            }
        }
    }

    let body = parts.join(",\n");

    // MySQL table comment is inline
    let table_comment = if !options.nocomments && target_dialect == Dialect::Mysql {
        table
            .comment
            .as_ref()
            .map(|c| format!(" COMMENT '{}'", c.replace('\'', "''")))
            .unwrap_or_default()
    } else {
        String::new()
    };

    format!("CREATE TABLE {qname} (\n{body}\n){table_comment};")
}

/// Generate a column definition line.
fn generate_column_def(
    col: &ColumnInfo,
    constraints: &[ConstraintInfo],
    source_dialect: Dialect,
    target_dialect: Dialect,
) -> String {
    let qname = quote_identifier(&col.name, target_dialect);

    // Detect auto-increment
    let is_auto = col.is_identity
        || col.autoincrement == Some(true)
        || col
            .column_default
            .as_deref()
            .map(|d| is_serial_default(d, source_dialect))
            .unwrap_or(false);

    let is_pk = is_primary_key_column(&col.name, constraints);

    // Type
    let type_str = if is_auto {
        format_autoincrement_type(col, source_dialect, target_dialect, is_pk)
    } else {
        ddl_typemap::map_ddl_type(col, source_dialect, target_dialect).sql_type
    };

    let mut parts = vec![format!("    {qname} {type_str}")];

    // NOT NULL (skip for auto-increment PKs where NOT NULL is implied)
    if !col.is_nullable && !(is_auto && is_pk) {
        parts.push("NOT NULL".to_string());
    }

    // DEFAULT (skip for auto-increment columns)
    if !is_auto {
        if let Some(ref default) = col.column_default {
            let ddl_default =
                format_ddl_default(default, source_dialect, target_dialect);
            parts.push(format!("DEFAULT {ddl_default}"));
        }
    }

    // Auto-increment suffix (MySQL, MSSQL, SQLite)
    if is_auto {
        let suffix = format_autoincrement_suffix(col, target_dialect, is_pk);
        if !suffix.is_empty() {
            parts.push(suffix);
        }
    }

    // MySQL inline column comment
    if target_dialect == Dialect::Mysql {
        if let Some(ref comment) = col.comment {
            parts.push(format!("COMMENT '{}'", comment.replace('\'', "''")));
        }
    }

    parts.join(" ")
}

/// Get the type string for an auto-increment column, potentially overriding the base type.
fn format_autoincrement_type(
    col: &ColumnInfo,
    source_dialect: Dialect,
    target_dialect: Dialect,
    is_pk: bool,
) -> String {
    let base_type = ddl_typemap::map_ddl_type(col, source_dialect, target_dialect).sql_type;

    match target_dialect {
        Dialect::Postgres => {
            // Use SERIAL/BIGSERIAL which implies the type
            if base_type.contains("BIG") {
                "BIGSERIAL".to_string()
            } else {
                "SERIAL".to_string()
            }
        }
        Dialect::Sqlite if is_pk => {
            // SQLite AUTOINCREMENT only works with INTEGER PRIMARY KEY
            "INTEGER".to_string()
        }
        _ => base_type,
    }
}

/// Get the auto-increment suffix to append after the type.
fn format_autoincrement_suffix(
    col: &ColumnInfo,
    target_dialect: Dialect,
    is_pk: bool,
) -> String {
    match target_dialect {
        Dialect::Postgres => String::new(), // SERIAL/BIGSERIAL handles it
        Dialect::Mysql => "AUTO_INCREMENT".to_string(),
        Dialect::Sqlite if is_pk => "PRIMARY KEY AUTOINCREMENT".to_string(),
        Dialect::Sqlite => String::new(),
        Dialect::Mssql => {
            let (start, inc) = col
                .identity
                .as_ref()
                .map(|id| (id.start, id.increment))
                .unwrap_or((1, 1));
            format!("IDENTITY({start}, {inc})")
        }
    }
}

/// Format a default value expression for the target dialect.
fn format_ddl_default(default: &str, source_dialect: Dialect, target_dialect: Dialect) -> String {
    // Step 1: Strip source-specific syntax
    let cleaned = match source_dialect {
        Dialect::Postgres => super::strip_pg_typecast(default),
        Dialect::Mssql => super::strip_mssql_parens(default),
        Dialect::Mysql | Dialect::Sqlite => default.trim(),
    };

    // Step 2: Translate common function names
    translate_default_function(cleaned, target_dialect)
}

/// Translate common SQL functions between dialects.
fn translate_default_function(expr: &str, target: Dialect) -> String {
    let lower = expr.trim().to_lowercase();

    // Normalize common timestamp functions
    if lower == "now()"
        || lower == "current_timestamp"
        || lower == "getdate()"
        || lower == "sysdatetimeoffset()"
    {
        return match target {
            Dialect::Postgres => "now()".to_string(),
            Dialect::Mysql | Dialect::Sqlite => "CURRENT_TIMESTAMP".to_string(),
            Dialect::Mssql => "GETDATE()".to_string(),
        };
    }

    // UUID generators
    if lower == "gen_random_uuid()" || lower == "uuid()" || lower == "newid()" {
        return match target {
            Dialect::Postgres => "gen_random_uuid()".to_string(),
            Dialect::Mysql => "(UUID())".to_string(),
            Dialect::Mssql => "NEWID()".to_string(),
            Dialect::Sqlite => "NULL".to_string(), // No native UUID in SQLite
        };
    }

    // Pass through as-is (string literals, numbers, etc.)
    expr.to_string()
}

/// Generate a qualified table name with schema prefix if non-default.
fn qualified_table_name(schema: &str, table: &str, dialect: Dialect) -> String {
    let default_schema = dialect.default_schema();
    if schema.is_empty() || schema == default_schema {
        quote_identifier(table, dialect)
    } else {
        format!(
            "{}.{}",
            quote_identifier(schema, dialect),
            quote_identifier(table, dialect)
        )
    }
}

/// Generate CREATE INDEX statements for a table.
fn generate_indexes(table: &TableInfo, target_dialect: Dialect) -> Vec<String> {
    let mut stmts = Vec::new();
    let tname = qualified_table_name(&table.schema, &table.name, target_dialect);

    for idx in &table.indexes {
        if is_unique_constraint_index(idx, &table.constraints) {
            continue;
        }

        let unique = if idx.is_unique { "UNIQUE " } else { "" };
        let cols: Vec<String> = idx
            .columns
            .iter()
            .map(|c| quote_identifier(c, target_dialect))
            .collect();
        stmts.push(format!(
            "CREATE {unique}INDEX {} ON {tname} ({});",
            quote_identifier(&idx.name, target_dialect),
            cols.join(", ")
        ));
    }

    stmts
}

/// Generate COMMENT ON statements (PG only; MySQL is inline).
fn generate_comments(table: &TableInfo, target_dialect: Dialect) -> Vec<String> {
    // Only PG uses separate COMMENT ON statements
    if target_dialect != Dialect::Postgres {
        return vec![];
    }

    let mut stmts = Vec::new();
    let tname = qualified_table_name(&table.schema, &table.name, target_dialect);

    if let Some(ref comment) = table.comment {
        stmts.push(format!(
            "COMMENT ON TABLE {tname} IS '{}';",
            comment.replace('\'', "''")
        ));
    }

    for col in &table.columns {
        if let Some(ref comment) = col.comment {
            stmts.push(format!(
                "COMMENT ON COLUMN {tname}.{} IS '{}';",
                quote_identifier(&col.name, target_dialect),
                comment.replace('\'', "''")
            ));
        }
    }

    stmts
}

/// Generate CREATE TYPE statements for enums (PG target only).
fn generate_enum_types(schema: &IntrospectedSchema, target_dialect: Dialect) -> Vec<String> {
    if target_dialect != Dialect::Postgres || schema.enums.is_empty() {
        return vec![];
    }

    schema
        .enums
        .iter()
        .map(|e| {
            let qname = quote_identifier(&e.name, target_dialect);
            let values: Vec<String> = e
                .values
                .iter()
                .map(|v| format!("'{}'", v.replace('\'', "''")))
                .collect();
            format!("CREATE TYPE {qname} AS ENUM ({});", values.join(", "))
        })
        .collect()
}

/// Detect circular FK dependencies among tables.
/// Keys by (schema, name) to handle multi-schema introspection correctly.
fn detect_fk_cycles(tables: &[TableInfo]) -> bool {
    use std::collections::HashSet;

    type Key<'a> = (&'a str, &'a str);

    let table_keys: HashSet<Key> = tables
        .iter()
        .map(|t| (t.schema.as_str(), t.name.as_str()))
        .collect();
    let mut visited: HashSet<Key> = HashSet::new();
    let mut in_stack: HashSet<Key> = HashSet::new();

    fn dfs<'a>(
        node: Key<'a>,
        adj: &HashMap<Key<'a>, Vec<Key<'a>>>,
        visited: &mut HashSet<Key<'a>>,
        in_stack: &mut HashSet<Key<'a>>,
    ) -> bool {
        visited.insert(node);
        in_stack.insert(node);
        if let Some(neighbors) = adj.get(&node) {
            for &neighbor in neighbors {
                if !visited.contains(&neighbor) {
                    if dfs(neighbor, adj, visited, in_stack) {
                        return true;
                    }
                } else if in_stack.contains(&neighbor) {
                    return true;
                }
            }
        }
        in_stack.remove(&node);
        false
    }

    let mut adj: HashMap<Key, Vec<Key>> = HashMap::new();
    for table in tables {
        let src = (table.schema.as_str(), table.name.as_str());
        for c in &table.constraints {
            if c.constraint_type == ConstraintType::ForeignKey {
                if let Some(ref fk) = c.foreign_key {
                    let dst = (fk.ref_schema.as_str(), fk.ref_table.as_str());
                    if table_keys.contains(&dst) && dst != src {
                        adj.entry(src).or_default().push(dst);
                    }
                }
            }
        }
    }

    for table in tables {
        let key = (table.schema.as_str(), table.name.as_str());
        if !visited.contains(&key) {
            if dfs(key, &adj, &mut visited, &mut in_stack) {
                return true;
            }
        }
    }
    false
}

/// Diff two schemas and emit ALTER statements.
/// Phase 1: tables and columns only.
fn diff_schemas(
    source: &IntrospectedSchema,
    target: &IntrospectedSchema,
    options: &DdlOptions,
) -> String {
    let source_dialect = source.dialect;
    let target_dialect = options.target_dialect;

    // Key by (schema, table_name) to handle multi-schema introspection
    let source_map: HashMap<(&str, &str), &TableInfo> = source
        .tables
        .iter()
        .map(|t| ((t.schema.as_str(), t.name.as_str()), t))
        .collect();
    let target_map: HashMap<(&str, &str), &TableInfo> = target
        .tables
        .iter()
        .map(|t| ((t.schema.as_str(), t.name.as_str()), t))
        .collect();

    let mut stmts: Vec<String> = Vec::new();

    // New tables (in source, not in target)
    let sorted_source = topo_sort_tables(&source.tables);
    for table in &sorted_source {
        if table.table_type != TableType::Table {
            continue;
        }
        let key = (table.schema.as_str(), table.name.as_str());
        if !target_map.contains_key(&key) {
            stmts.push(generate_create_table(
                table,
                source_dialect,
                target_dialect,
                options,
            ));
            if !options.noindexes {
                stmts.extend(generate_indexes(table, target_dialect));
            }
        }
    }

    // Modified tables (in both): compare columns
    for table in &sorted_source {
        if table.table_type != TableType::Table {
            continue;
        }
        let key = (table.schema.as_str(), table.name.as_str());
        if let Some(target_table) = target_map.get(&key) {
            let alters = diff_table_columns(
                table,
                target_table,
                source_dialect,
                target_dialect,
            );
            stmts.extend(alters);
        }
    }

    // Dropped tables (in target, not in source)
    let mut dropped: Vec<(&str, &str)> = target_map
        .keys()
        .filter(|key| !source_map.contains_key(*key))
        .copied()
        .collect();
    dropped.sort();
    for (schema, name) in dropped {
        let qname = qualified_table_name(schema, name, target_dialect);
        stmts.push(format!("-- WARNING: destructive operation\nDROP TABLE IF EXISTS {qname};"));
    }

    if stmts.is_empty() {
        "-- No schema changes detected.\n".to_string()
    } else {
        let header = format!(
            "-- Generated by uvg (diff)\n-- Source: {source_dialect}, Target: {target_dialect}\n\n"
        );
        format!("{header}{}\n", stmts.join("\n\n"))
    }
}

/// Compare columns between source and target tables, emit ALTER statements.
fn diff_table_columns(
    source: &TableInfo,
    target: &TableInfo,
    source_dialect: Dialect,
    target_dialect: Dialect,
) -> Vec<String> {
    let mut stmts = Vec::new();
    let tname = qualified_table_name(&source.schema, &source.name, target_dialect);

    let source_cols: HashMap<&str, &ColumnInfo> =
        source.columns.iter().map(|c| (c.name.as_str(), c)).collect();
    let target_cols: HashMap<&str, &ColumnInfo> =
        target.columns.iter().map(|c| (c.name.as_str(), c)).collect();

    // New columns (in source, not in target)
    for col in &source.columns {
        if !target_cols.contains_key(col.name.as_str()) {
            let col_def = generate_column_def(
                col,
                &source.constraints,
                source_dialect,
                target_dialect,
            );
            // Strip leading whitespace from column def
            let col_def = col_def.trim();
            let add_clause = match target_dialect {
                Dialect::Mssql => "ADD",
                _ => "ADD COLUMN",
            };
            stmts.push(format!("ALTER TABLE {tname} {add_clause} {col_def};"));
        }
    }

    // Modified columns (in both): check type, nullable, default
    for col in &source.columns {
        if let Some(target_col) = target_cols.get(col.name.as_str()) {
            let alters = diff_column(col, target_col, &source.schema, &source.name, source_dialect, target_dialect);
            stmts.extend(alters);
        }
    }

    // Dropped columns (in target, not in source)
    let mut dropped: Vec<&str> = target_cols
        .keys()
        .filter(|name| !source_cols.contains_key(*name))
        .copied()
        .collect();
    dropped.sort();
    for name in dropped {
        let qcol = quote_identifier(name, target_dialect);
        stmts.push(format!(
            "-- WARNING: destructive operation\nALTER TABLE {tname} DROP COLUMN {qcol};"
        ));
    }

    stmts
}

/// Compare a single column and emit ALTER statements if different.
/// Compares type, nullability, and default values.
fn diff_column(
    source: &ColumnInfo,
    target: &ColumnInfo,
    table_schema: &str,
    table_name: &str,
    source_dialect: Dialect,
    target_dialect: Dialect,
) -> Vec<String> {
    let mut stmts = Vec::new();
    let tname = qualified_table_name(table_schema, table_name, target_dialect);
    let cname = quote_identifier(&source.name, target_dialect);

    let source_type = ddl_typemap::map_ddl_type(source, source_dialect, target_dialect);
    let target_type = ddl_typemap::map_ddl_type(target, target_dialect, target_dialect);

    let type_changed = source_type.sql_type != target_type.sql_type;
    let nullable_changed = source.is_nullable != target.is_nullable;

    // Compare defaults (strip dialect-specific syntax before comparing)
    let source_default = source
        .column_default
        .as_deref()
        .map(|d| format_ddl_default(d, source_dialect, target_dialect));
    let target_default = target
        .column_default
        .as_deref()
        .map(|d| format_ddl_default(d, target_dialect, target_dialect));
    let default_changed = source_default != target_default;

    if !type_changed && !nullable_changed && !default_changed {
        return stmts;
    }

    match target_dialect {
        Dialect::Postgres => {
            if type_changed {
                stmts.push(format!(
                    "ALTER TABLE {tname} ALTER COLUMN {cname} TYPE {};",
                    source_type.sql_type
                ));
            }
            if nullable_changed {
                if source.is_nullable {
                    stmts.push(format!(
                        "ALTER TABLE {tname} ALTER COLUMN {cname} DROP NOT NULL;"
                    ));
                } else {
                    stmts.push(format!(
                        "ALTER TABLE {tname} ALTER COLUMN {cname} SET NOT NULL;"
                    ));
                }
            }
            if default_changed {
                match &source_default {
                    Some(d) => stmts.push(format!(
                        "ALTER TABLE {tname} ALTER COLUMN {cname} SET DEFAULT {d};"
                    )),
                    None => stmts.push(format!(
                        "ALTER TABLE {tname} ALTER COLUMN {cname} DROP DEFAULT;"
                    )),
                }
            }
        }
        Dialect::Mysql => {
            // MySQL requires full column redefinition with MODIFY
            let not_null = if !source.is_nullable { " NOT NULL" } else { "" };
            let default_clause = match &source_default {
                Some(d) => format!(" DEFAULT {d}"),
                None => String::new(),
            };
            stmts.push(format!(
                "ALTER TABLE {tname} MODIFY COLUMN {cname} {}{not_null}{default_clause};",
                source_type.sql_type
            ));
        }
        Dialect::Mssql => {
            if type_changed || nullable_changed {
                let not_null = if !source.is_nullable { " NOT NULL" } else { " NULL" };
                stmts.push(format!(
                    "ALTER TABLE {tname} ALTER COLUMN {cname} {}{not_null};",
                    source_type.sql_type
                ));
            }
            if default_changed {
                // MSSQL defaults are named constraints; drop existing then add new
                stmts.push(format!(
                    "-- NOTE: MSSQL requires dropping the named default constraint first.\n-- Run: SELECT name FROM sys.default_constraints WHERE parent_object_id = OBJECT_ID('{tname_raw}') AND col_name(parent_object_id, parent_column_id) = '{col_name}'\n-- Then: ALTER TABLE {tname} DROP CONSTRAINT <name>;",
                    tname_raw = table_name,
                    col_name = source.name
                ));
                if let Some(ref d) = source_default {
                    stmts.push(format!(
                        "ALTER TABLE {tname} ADD DEFAULT {d} FOR {cname};"
                    ));
                }
            }
        }
        Dialect::Sqlite => {
            stmts.push(format!(
                "-- WARNING: SQLite does not support ALTER COLUMN. Table recreation required.\n-- ALTER TABLE {tname} ALTER COLUMN {cname} TYPE {};",
                source_type.sql_type
            ));
        }
    }

    stmts
}

